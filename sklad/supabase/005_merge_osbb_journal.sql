-- ── RPC-функції (та сама поведінка, що й supabase/*.sql, тільки таблиці
-- перейменовані на osbb_app_auth/osbb_app_pin_attempts) ────────────────────
create or replace function verify_lock_pin(attempt text)
returns boolean
language plpgsql
security definer
set search_path = public, extensions
as $$
declare
  attempt_row osbb_app_pin_attempts%rowtype;
  ok boolean;
  next_failed_count int;
begin
  select * into attempt_row from osbb_app_pin_attempts where pin_name = 'lock';

  if attempt_row.locked_until is not null and attempt_row.locked_until > now() then
    return false;
  end if;

  select exists (
    select 1 from osbb_app_auth
    where id = 1 and lock_pin_hash = crypt(attempt, lock_pin_hash)
  ) into ok;

  if ok then
    delete from osbb_app_pin_attempts where pin_name = 'lock';
    return true;
  end if;

  next_failed_count := case
    when attempt_row.pin_name is null or attempt_row.last_failed_at < now() - interval '15 minutes' then 1
    else attempt_row.failed_count + 1
  end;

  insert into osbb_app_pin_attempts (pin_name, failed_count, locked_until, last_failed_at)
  values (
    'lock',
    next_failed_count,
    case when next_failed_count >= 5 then now() + interval '5 minutes' else null end,
    now()
  )
  on conflict (pin_name) do update
    set failed_count = excluded.failed_count,
        locked_until = excluded.locked_until,
        last_failed_at = excluded.last_failed_at;

  return false;
end;
$$;

