-- ── Telegram-сповіщення чату журналу: bot_token спільний з уже існуючою
-- telegram_config складу, chat_id -- з osbb_telegram_config. Заразом
-- виправляє реальний баг зі старого проєкту журналу: оригінальний
-- trg_notify_chat звертався до new.message, якої в таблиці chat не існує
-- (є тільки new.text) -- тому сповіщення чату там мовчки ніколи не йшли.
create or replace function notify_osbb_telegram(msg text)
returns void
language plpgsql
security definer
set search_path = public, extensions
as $$
declare
  v_token text;
  v_chat_id text;
begin
  select bot_token into v_token from telegram_config where id = 1;
  select chat_id into v_chat_id from osbb_telegram_config where id = 1;
  if v_token is null or v_chat_id is null then
    return;
  end if;

  perform net.http_post(
    url := 'https://api.telegram.org/bot' || v_token || '/sendMessage',
    body := jsonb_build_object('chat_id', v_chat_id, 'text', msg),
    headers := jsonb_build_object('Content-Type', 'application/json')
  );
exception when others then
  null;
end;
$$;

create or replace function trg_notify_chat()
returns trigger
language plpgsql
security definer
set search_path = public, extensions
as $$
begin
  perform notify_osbb_telegram(
    '💬 Нове повідомлення в чаті ОСББ від ' || coalesce(new.author, 'Анонім') || ': ' || coalesce(new.text, '')
  );
  return new;
exception when others then
  return new;
end;
$$;

drop trigger if exists chat_telegram_notify on chat;
create trigger chat_telegram_notify after insert on chat for each row execute function trg_notify_chat();

